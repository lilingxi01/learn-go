// Package main is the entry point for executable programs
package main

// Import the fmt (format) package for printing
import "fmt"

// main is the function that runs when the program starts
func main() {
	// Print "Hello, World!" to the console
	fmt.Println("Hello, World!")

	// You can print multiple things
	fmt.Println("Welcome to Go!")

	// fmt.Println automatically adds a newline at the end
	fmt.Println("Let's learn Go together.")
}
